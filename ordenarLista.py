#Función ordenar max
def ordenar_max(acumulado):
    for b in range(len(acumulado)):
        for c in range(len(acumulado)):
            if acumulado[b] > acumulado[c]:
                temp = acumulado[b]
                acumulado[b] = acumulado[c]
                acumulado[c] = temp

    print("Lista ordenada",end=": ")
    for d in range(len(acumulado)):
        print(acumulado[d], end=": ")
    print("Fin!")

def ordenar_min(acumulado):
    for e in range(len(acumulado)):
        for tigrillo in range(len(acumulado)):
            if acumulado[e] < acumulado[tigrillo]:
                temp = acumulado[tigrillo]
                acumulado[tigrillo] = acumulado[e]
                acumulado[e] = temp

    print("Lista ordenada",end=": ")
    for d in range(len(acumulado)):
        print(acumulado[d], end=": ")
    print("Fin!!")

